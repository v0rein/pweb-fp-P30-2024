import mongoose from "mongoose";

export enum Role {
  USER = "USER",
  ADMIN = "ADMIN",
}

const UserSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      enum: Object.values(Role),
      required: true,
    },
    tokens: [
      {
        token: {
          type: String,
          required: true,
        },
      },
    ],
  },
  { timestamps: true }
);

export const User = mongoose.model("Users", UserSchema);
